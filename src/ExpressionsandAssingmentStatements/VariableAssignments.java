package ExpressionsandAssingmentStatements;

public class VariableAssignments {
    public static void main(String[] args) {
        int x = 3;
        int y = 2;
        System.out.println(x);
        System.out.println(y);
        y = x;
        x = 7;
        System.out.println(x);
        System.out.println(y);
        x = 5;
        y = 9;
        System.out.println(x);
        System.out.println(y);
    }
}
