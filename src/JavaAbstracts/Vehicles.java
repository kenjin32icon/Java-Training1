package JavaAbstracts;

public class Vehicles {
    //Instance variables(Attributes that belong to the Vehicle class and represent a vehicle)
    private String name;
    private int year;
    private String color;
    private double price;

    //Constructors for the instance variables to create a reusable bundle of logic for the vehicle attributes
    public Vehicles(String name, int year, String color, double price) {
        this.name = name;
        this.year = year;
        this.color = color;
        this.price = price;
    }

    //Getters and setters

}
