// Greater than or equal to operator >= : This operator is used to check if the left operand is greater than or equal to the right operand.
public class Greaterthanorequaltooperator {
    int a = 20;
    int b = 20;
    boolean result = (a >= b);
    public static void main(String[] arg){
        Greaterthanorequaltooperator obj = new Greaterthanorequaltooperator();
        System.out.println(obj.result);
    }
}
